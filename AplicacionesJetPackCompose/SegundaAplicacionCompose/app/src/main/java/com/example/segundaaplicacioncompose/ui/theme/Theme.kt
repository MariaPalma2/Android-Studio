@Composable
fun SegundaAplicacionComposeTheme(
    darkTheme: Boolean = false, // Parametro para seleccionar el tema oscuro o claro
    content: @Composable () -> Unit
) {
    val colorScheme = if (darkTheme) DarkColorScheme else LightColorScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content
    )
}